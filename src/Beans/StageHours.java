package Beans;

public class StageHours {
	/**
	 * The name of day
	 */
	private String nameDay;
	
	/**
	 * the begin of hours AM
	 */
	private String heurBeginAm;
	
	/**
	 * The end of hour AM
	 */
	private String heureEndAm;
	
	/**
	 * The begin of hour PM
	 */
	private String heureBeginPm;
	
	/**
	 * The end of hour PM
	 */
	private String heureEndPm;

	public String getNameDay() {
		return nameDay;
	}

	public void setNameDay(String nameDay) {
		this.nameDay = nameDay;
	}

	public String getHeurBeginAm() {
		return heurBeginAm;
	}

	public void setHeurBeginAm(String heurBeginAm) {
		this.heurBeginAm = heurBeginAm;
	}

	public String getHeureEndAm() {
		return heureEndAm;
	}

	public void setHeureEndAm(String heureEndAm) {
		this.heureEndAm = heureEndAm;
	}

	public String getHeureBeginPm() {
		return heureBeginPm;
	}

	public void setHeureBeginPm(String heureBeginPm) {
		this.heureBeginPm = heureBeginPm;
	}

	public String getHeureEndPm() {
		return heureEndPm;
	}

	public void setHeureEndPm(String heureEndPm) {
		this.heureEndPm = heureEndPm;
	}
	
	
}
